     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(ResetPasswordRequest $request)
    {
        $input = $request->only('password', 'email');
        User::where('email', $input['email'])->update(['password' => bcrypt($input['password'])]);
        return redirect()->route('login.index');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $passwordReset = $this->userRepository->checkToken($id);
        if ($passwordReset) {
            return view('accounts.reset_password', compact('passwordReset'));
        }

        return redirect()->route('login.index');
    }

